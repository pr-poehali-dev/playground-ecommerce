const ageRanges = ['1-3 года', '2-5 лет', '3-7 лет', '4-10 лет', '5-12 лет', '6-14 лет', '3-12 лет', '1-5 лет'];

function generateProducts(): Product[] {
  const products: Product[] = [];
  
  for (let i = 1; i <= 100; i++) {
    const basePrice = Math.floor(Math.random() * (400000 - 50000) + 50000);
    const roundedPrice = Math.round(basePrice / 5000) * 5000;
    
    const hasDiscount = Math.random() > 0.7;
    const isNew = Math.random() > 0.8;
    
    products.push({
      id: i,
      name: `${names[i % names.length]} ${i > names.length ? Math.floor(i / names.length) : ''}`.trim(),
      price: roundedPrice,
      image: imagePool[i % imagePool.length],
      description: descriptions[i % descriptions.length],
      ageRange: ageRanges[i % ageRanges.length],
      isNew: isNew,
      discount: hasDiscount ? [10, 15, 20, 25][Math.floor(Math.random() * 4)] : undefined,
    });
  }
  
  return products;
}

export const PRODUCTS = generateProducts();
